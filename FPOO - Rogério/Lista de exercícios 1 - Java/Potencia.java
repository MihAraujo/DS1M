class Potencia {
    public static void main (String[] args) {
        int numero1 = 5;
        int numero2 = 3;
    
        double potencia1 = Math.pow(numero1, 5);
        double potencia2 = Math.pow(numero2, 5);

        System.out.println("Potência 1: " + potencia1);
        System.out.println("Potência 2: " + potencia2);
    }
}