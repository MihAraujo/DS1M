class QuatroOperacoes {
    public static void main(String[] args) {
        double decimal1 = 1.5;
        double decimal2 = 3.5;

        double adicao = decimal1 + decimal2;
        double subtracao = decimal1 - decimal2;
        double multiplicacao = decimal1 * decimal2;
        double divisao = decimal1 / decimal2;

        System.out.println("Adição igual a:" + adicao);
        System.out.println("Subtração igual a:" + subtracao);
        System.out.println("Multiplicação igual a:" + multiplicacao);
        System.out.println("Divisão igual a:" + divisao);
    }
}